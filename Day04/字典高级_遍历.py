personDict={'name':'小明','age':18,'sex':'男'}

# 获取字典中的key
for i in personDict.keys():
    print(i)

# 获取字典中的value
for i in personDict.values():
    print(i)

# 获取字典中的 key 和 value
for i,j in personDict.items():
    print(i,j)

# 获取字典中的键值对
for i in personDict.items():
    print(i)






